let myNumberOne = 10
let myNumberTwo = 20
let myNumberThree = 15
let myNumberFour = 50

console.log(myNumberOne = myNumberTwo)
console.log(myNumberOne == myNumberTwo)
console.log(myNumberThree == myNumberFour)