/*
    MATH // Sempre com o 'M' maíusculo
*/

console.log(Math.PI) // Mostra o valor de PI
console.log(Math.pow(5,8)) // Permite calcularmos em potênciação
console.log(Math.sqrt(9,3)) // Permite calcularmos em raízes, seja ela quadrada, cúbica e assim em diante
console.log(Math.random) // Exibe um valor aleatório entre 0 e 1 toda vez que for acionado
console.log(Math.ceil(25.5)) // Sempre arredonda o valor para "CIMA"
console.log(Math.floor(25.5)) // Sempre arredonda o valor para "BAIXO"