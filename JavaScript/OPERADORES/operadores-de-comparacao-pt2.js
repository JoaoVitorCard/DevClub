/*
    OPERADORES DE COMPARAÇÃO

    >   -> MAIOR
    >=  -> MAIOR OU IGUAL
    <   -> MENOR
    <=  -> MENOR OU IGUAL
*/

const myNumber = 15
const mySecondName = 15

console.log(myNumber > mySecondName)
console.log(myNumber >= mySecondName)
console.log(myNumber < mySecondName)
console.log(myNumber <= mySecondName)