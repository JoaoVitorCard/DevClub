/* 
 OPERADORES DE ATRIBUIÇÃO

    =  -> Atribuição
    += -> Adição
    -= -> Subtração
    /= -> Divisão
    *= -> Multiplicação
    %= -> Resto da divisão
*/

// =  -> Atribuição

let number1 = 1
let number2 = 2

console.log(number1 + number2)

// += -> Adição

let number3 = 5

number3 += 15

console.log(number3)

// -= -> Subtração

let number4 = 5

number4 -= 15

console.log(number4)

// /= -> Divisão

let number5 = 15

number5 /= 5

console.log(number5)

// *= -> Multiplicação

let number6 = 15

number6 *= 5

console.log(number6)

// %= -> Resto da divisão

let number7 = 15

number7 %= 5

console.log(number7)