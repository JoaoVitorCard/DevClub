/* 
Operadores de comparação

  >  -> Maior que
  <  -> Menor que

  >= -> Maior ou igual a
  <= -> Menor ou igual a
*/

const firstNumber = 10
const secondNumber = 20

console.log(firstNumber > secondNumber)
console.log(firstNumber < secondNumber)
console.log(firstNumber >= secondNumber)
console.log(firstNumber <= secondNumber)
